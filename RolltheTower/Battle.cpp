bool replaceDeadMonster(Player& player, Monster*& monster) {
    Monster* nextMonster = monster->getNextMonsterOnDeath();

    if (nextMonster == nullptr) {
        monster->onDeath(player);
        return false;
    }

    monster->onDeath(player);
    delete monster;
    monster = nextMonster;
    cout << "\n===== " << monster->getName() << " 등장! =====\n";
    return true;
}

bool Battle::startMonsterBattle(Player& player, Biome* biome, int stage) {
    lastMonsterEscaped = false;
    Monster* monster = biome->spawnMonster();
    cout << "\n===== " << monster->getName() << " 등장! =====\n";
    cout << "[" << biome->getName() << " 지형]\n";

    bool hasIntent = false;
    bool skipMonsterTurn = false;
    bool poisonAppliedThisTurn = false;

    while (!player.isDead() && !monster->isDead()) {
        if (!skipMonsterTurn) {
            monster->clearBlock();
            monster->onTurnStart();

            if (hasIntent) {
                monster->executeIntent(player);
                biome->onTurnEnd(player, monster);

                if (player.isDead()) {
                    break;
                }

                // 화상은 몬스터 행동이 끝난 뒤 발동
                monster->applyBurnDamage();

                if (monster->isDead()) {
                    cout << monster->getName() << "이(가) 화상 피해로 쓰러졌습니다!\n";

                    if (replaceDeadMonster(player, monster)) {
                        hasIntent = false;
                        poisonAppliedThisTurn = false;
                        system("pause");
                        continue;
                    }

                    break;
                }
            }

            monster->decideIntent();
        }

        skipMonsterTurn = false;

        Weapon* equippedWeapon = player.getEquippedWeapon();

        monster->onBeforePlayerTurn(player);
        printBattleScreen(player, monster, biome, false);
        hasIntent = true;

        if (equippedWeapon != nullptr) {
            cout << "\n[ 행동 선택 ]\n";
            cout << "1. " << equippedWeapon->getBattleActionText(1) << "\n";
            cout << "2. " << equippedWeapon->getBattleActionText(2) << "\n";
            cout << "3. 수비 - 방어 10\n";
            cout << "4. 인벤토리 - 아이템 사용\n";
            cout << "선택: ";
        }
        else {
            cout << "\n[ 행동 선택 ]\n";
            cout << "1. 타격 - 공격 6\n";
            cout << "2. 강타 - 공격 4, 취약 2 부여\n";
            cout << "3. 수비 - 방어 10\n";
            cout << "4. 인벤토리 - 아이템 사용\n";
            cout << "선택: ";
        }

        int choice;
        cin >> choice;

        if (!poisonAppliedThisTurn && (choice == 1 || choice == 2 || choice == 3 || choice == 4)) {
            player.applyPoisonBeforeAction();
            poisonAppliedThisTurn = true;

            if (player.isDead()) {
                break;
            }
        }

        if (choice == 1 || choice == 2) {
            if (biome->isAttackMissed()) {
                cout << "공격이 빗나갔습니다.\n";
            }
            else if (equippedWeapon != nullptr) {
                equippedWeapon->useBattleAction(player, *monster, choice);
            }
            else {
                cout << "장착한 무기가 없어 공격할 수 없습니다.\n";
                skipMonsterTurn = true;
                system("pause");
                continue;
            }
        }
        else if (choice == 3) {
            player.addBlock(10);
            cout << "플레이어가 방어 10을 얻었습니다.\n";
        }
        else if (choice == 4) {
            player.showInventory();
            cout << "사용할 아이템 번호 (0: 취소): ";

            int itemIdx;
            cin >> itemIdx;

            if (itemIdx > 0) {
                bool isTurnConsumed = player.useItem(itemIdx - 1, *monster);

                if (!isTurnConsumed) {
                    cout << "\n[!] 포션을 사용했습니다. 턴이 소모되지 않습니다.\n";

                    if (monster->isDead()) {
                        cout << "\n전투 승리! " << monster->getName() << "을(를) 처치했습니다!\n";

                        if (replaceDeadMonster(player, monster)) {
                            hasIntent = false;
                            poisonAppliedThisTurn = false;
                            system("pause");
                            continue;
                        }

                        break;
                    }

                    skipMonsterTurn = true;
                    system("pause");
                    continue;
                }
            }
            else {
                cout << "아이템 사용을 취소했습니다.\n";
                skipMonsterTurn = true;
                system("pause");
                continue;
            }
        }
        else {
            cout << "잘못된 입력입니다. 다시 선택하세요.\n";
            skipMonsterTurn = true;
            system("pause");
            continue;
        }

        if (monster->isDead()) {
            cout << "\n전투 승리! " << monster->getName() << "을(를) 처치했습니다!\n";

            if (replaceDeadMonster(player, monster)) {
                hasIntent = false;
                poisonAppliedThisTurn = false;
                system("pause");
                continue;
            }

            break;
        }

        // 플레이어 행동이 끝난 뒤 화상 발동
        player.applyBurnAfterAction();

        if (player.isDead()) {
            break;
        }

        monster->onPlayerActionEnd(player);

        player.decreaseDebuffs();
        poisonAppliedThisTurn = false;

        system("pause");
    }

    bool playerWon = !player.isDead();
    if (monster->hasEscaped()) {
        lastMonsterEscaped = true;
        delete monster;
        return true;
    }

    if (playerWon && player.getStatus().getParasite() > 0) {
        cout << "\n[기생] 전투가 끝나 기생 디버프가 사라졌습니다.\n";
        player.getStatus().clearParasite();
    }

    delete monster;

    return playerWon;
}

bool Battle::startBossBattle(Player& player, Biome* biome) {
    Monster* boss = biome->spawnBossMonster();

    cout << "\n================================\n";
    cout << "보스 몬스터가 나타났다!\n";
    cout << boss->getName() << "이(가) 등장했습니다!\n";

    bool hasIntent = false;
    bool skipMonsterTurn = false;
    bool poisonAppliedThisTurn = false;

    while (!player.isDead() && !boss->isDead()) {
        if (!skipMonsterTurn) {
            boss->clearBlock();
            boss->onTurnStart();

            if (hasIntent) {
                boss->executeIntent(player);

                if (player.isDead()) {
                    break;
                }

                // 화상은 보스 행동이 끝난 뒤 발동
                boss->applyBurnDamage();

                if (boss->isDead()) {
                    cout << boss->getName() << "이(가) 화상 피해로 쓰러졌습니다!\n";
                    boss->onDeath(player);
                    break;
                }
            }

            boss->decideIntent();
        }

        skipMonsterTurn = false;

        Weapon* equippedWeapon = player.getEquippedWeapon();

        boss->onBeforePlayerTurn(player);
        printBattleScreen(player, boss, biome, true);
        hasIntent = true;

        if (equippedWeapon != nullptr) {
            cout << "\n[ 행동 선택 ]\n";
            cout << "1. " << equippedWeapon->getBattleActionText(1) << "\n";
            cout << "2. " << equippedWeapon->getBattleActionText(2) << "\n";
            cout << "3. 수비 - 방어 10\n";
            cout << "4. 인벤토리 - 아이템 사용\n";
            cout << "선택: ";
        }
        else {
            cout << "\n[ 행동 선택 ]\n";
            cout << "1. 타격 - 공격 6\n";
            cout << "2. 강타 - 공격 4, 취약 2 부여\n";
            cout << "3. 수비 - 방어 10\n";
            cout << "4. 인벤토리 - 아이템 사용\n";
            cout << "선택: ";
        }

        int choice;
        cin >> choice;

        if (!poisonAppliedThisTurn && (choice == 1 || choice == 2 || choice == 3 || choice == 4)) {
            player.applyPoisonBeforeAction();
            poisonAppliedThisTurn = true;

            if (player.isDead()) {
                break;
            }
        }

        if (choice == 1 || choice == 2) {
            if (equippedWeapon != nullptr) {
                equippedWeapon->useBattleAction(player, *boss, choice);
            }
            else {
                cout << "장착한 무기가 없어 공격할 수 없습니다.\n";
                skipMonsterTurn = true;
                system("pause");
                continue;
            }
        }
        else if (choice == 3) {
            player.addBlock(10);
            cout << "플레이어가 방어 10을 얻었습니다.\n";
        }
        else if (choice == 4) {
            player.showInventory();
            cout << "사용할 아이템 번호 (0: 취소): ";

            int itemIdx;
            cin >> itemIdx;

            if (itemIdx > 0) {
                bool isTurnConsumed = player.useItem(itemIdx - 1, *boss);

                if (!isTurnConsumed) {
                    cout << "\n[!] 포션을 사용했습니다. 턴이 소모되지 않습니다.\n";

                    if (boss->isDead()) {
                        cout << "\n보스 전투 승리!\n";
                        boss->onDeath(player);
                        break;
                    }

                    skipMonsterTurn = true;
                    system("pause");
                    continue;
                }
            }
            else {
                cout << "아이템 사용을 취소했습니다.\n";
                skipMonsterTurn = true;
                system("pause");
                continue;
            }
        }
        else {
            cout << "잘못된 입력입니다. 다시 선택하세요.\n";
            skipMonsterTurn = true;
            system("pause");
            continue;
        }

        if (boss->isDead()) {
            cout << "\n보스 전투 승리!\n";
            boss->onDeath(player);
            break;
        }

        // 플레이어 행동이 끝난 뒤 화상 발동
        player.applyBurnAfterAction();

        if (player.isDead()) {
            break;
        }

        player.decreaseDebuffs();
        poisonAppliedThisTurn = false;

        system("pause");
    }

    bool playerWon = !player.isDead();

    if (playerWon && player.getStatus().getParasite() > 0) {
        cout << "\n[기생] 전투가 끝나 기생 디버프가 사라졌습니다.\n";
        player.getStatus().clearParasite();
    }

    delete boss;

    return playerWon;
}

Monster* Battle::createRandomMonster() {
    int type = rand() % 5;

    if (type == 0) {
        return new JawWorm();
    }
    else if (type == 1) {
        return new BallBug();
    }
    else if (type == 2) {
        return new SporeFungus();
    }
    else if (type == 3) {
        return new Mosquito();
    }

    return new InfectedHost();
}